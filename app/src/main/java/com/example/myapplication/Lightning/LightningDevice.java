package com.example.myapplication.Lightning;

public class LightningDevice {
    private String name,color;
    private int onOff,brithness;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getOnOff() {
        return onOff;
    }

    public void setOnOff(int onOff) {
        this.onOff = onOff;
    }

    public int getBrithness() {
        return brithness;
    }

    public void setBrithness(int brithness) {
        this.brithness = brithness;
    }
}
